def get_sorted_list(list_unsorted,list_sorted):
    sorting=[]
    for elem1 in list_unsorted:
        for ind,elem2 in enumerate(list_sorted):
            if (elem1 == elem2):break
            else: ind =-1       #raise error if element not found
        if (ind == -1): 
            print("!!!!!sorting error!!!!!, last entry: ",elem1)
            raise
        sorting.append(ind)
    list_sorted=[element for _,element in sorted(zip(sorting,list_unsorted))]
    return list_sorted

